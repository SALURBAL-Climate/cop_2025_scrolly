// Helper function to get combined data for a specific language
export function getCopRouteData(language = 'en') {
  const content = copRouteContent[language] || copRouteContent.en;
  return content.steps.map((step, index) => ({
    ...step,
    ...copRouteStaticData[index]
  }));
}

